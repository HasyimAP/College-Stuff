    private void jButtonEqActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEqActionPerformed
        bilKedua = Double.parseDouble(teksArea.getText());
        if (operasi == "+"){
            bilPertama = bilPertama + bilKedua;
            teksArea.setText(Double.toString(bilPertama));
        }
        else if (operasi == "-"){
            bilPertama = bilPertama - bilKedua;
            teksArea.setText(Double.toString(bilPertama));
        }
        else if (operasi == "*"){
            bilPertama = bilPertama * bilKedua;
            teksArea.setText(Double.toString(bilPertama));
        }
        else if (operasi == "/"){
            bilPertama = bilPertama / bilKedua;
            teksArea.setText(Double.toString(bilPertama));
        }
        else if (operasi == "%"){
            bilPertama = bilPertama % bilKedua;
            teksArea.setText(Double.toString(bilPertama));
        }
    }//GEN-LAST:event_jButtonEqActionPerformed

